#include "stdio.h"
void main()
{
	int n,i;
	double sum_series = 1;
	printf("\n Enter value of n - ");
	scanf("%d", &n);
	if(n > 0)
	{
		for(i = 2; i <= n; i++)
		{
			sum_series += 1/(double)i; 
		}
		printf("\n Sum Series - %.3f \n", sum_series);
	}
	else
		printf("\n n should be greater than 0 \n");
}
