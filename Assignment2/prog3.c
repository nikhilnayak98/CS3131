#include "stdio.h"
void main()
{
	int i,j;
	printf("\n Enter number of inputs - ");
	scanf("%d", &i);
	double nums[i], sum_squares = 0;
	printf("\n Enter numbers - \n");
	for(j = 0; j < i; j++)
	{
		scanf("%lf", &nums[i]);
		sum_squares += nums[i]*nums[i]; 
	}
	printf("\n Sum of squares - %.3f \n", sum_squares);
}
